def analyze_number_enhanced(number_str):
    number_str = re.sub(r'[^0-9]', '', number_str)
    
    if not number_str:
        return {"error": "请输入有效数字"}
    
    total = sum(int(d) for d in number_str)
    while total >= 10:
        total = sum(int(d) for d in str(total))
    
    shuli_num = int(number_str) % 81
    shuli_num = shuli_num if shuli_num != 0 else 81
    shuli_info = SHULI_JIXIONG.get(shuli_num, {"吉凶": "未知", "含义": "无"})
    
    digit_counts = {str(i): 0 for i in range(10)}
    for d in number_str:
        digit_counts[d] += 1
    
    wuxing_dist = {"金": 0, "木": 0, "水": 0, "火": 0, "土": 0}
    digit_wuxing = {
        "0": "水", "1": "水", "2": "土", "3": "木", "4": "木",
        "5": "土", "6": "火", "7": "火", "8": "金", "9": "金"
    }
    for d in number_str:
        wuxing_dist[digit_wuxing[d]] += 1
    
    patterns = []
    if len(number_str) >= 3:
        for i in range(len(number_str) - 2):
            sub = number_str[i:i+3]
            if sub == '111' or sub == '222' or sub == '333' or sub == '444' or sub == '555' or sub == '666' or sub == '777' or sub == '888' or sub == '999' or sub == '000':
                patterns.append(f"连号 {sub}")
            if int(sub[0]) + 1 == int(sub[1]) and int(sub[1]) + 1 == int(sub[2]):
                patterns.append(f"顺子 {sub}")
    
    if len(number_str) % 2 == 0:
        half = len(number_str) // 2
        if number_str[:half] == number_str[half:][::-1]:
            patterns.append("对称号")
    
    energy_info = {}
    for d in set(number_str):
        energy_info[d] = DIGIT_ENERGY.get(d, {})
    
    return {
        "number": number_str,
        "total": total,
        "shuli": {"number": shuli_num, **shuli_info},
        "digit_counts": digit_counts,
        "wuxing_dist": wuxing_dist,
        "patterns": patterns,
        "energy_info": energy_info
    }

def cast_gua(method="random"):
    if method == "random":
        yaos = []
        for _ in range(6):
            yaos.append(random.randint(6, 9))
    elif method == "manual":
        yaos = [random.randint(6, 9) for _ in range(6)]
    else:
        yaos = [random.randint(6, 9) for _ in range(6)]
    
    upper_gua = yaos[:3]
    lower_gua = yaos[3:]
    
    gua_code_upper = tuple(upper_gua)
    gua_code_lower = tuple(lower_gua)
    
    gua_name_map = {
        (9,9,9): "乾", (6,6,6): "坤", (9,9,6): "兑", (6,6,9): "艮",
        (9,6,9): "离", (6,9,6): "坎", (9,6,6): "震", (6,9,9): "巽"
    }
    
    lower_name = gua_name_map.get(gua_code_lower, "乾")
    upper_name = gua_name_map.get(gua_code_upper, "乾")
    
    hexagram_name = upper_name + lower_name
    
    change_yao = []
    for i, yao in enumerate(yaos):
        if yao == 6 or yao == 9:
            change_yao.append(i + 1)
    
    if change_yao:
        changed_yaos = []
        for yao in yaos:
            if yao == 6:
                changed_yaos.append(9)
            elif yao == 9:
                changed_yaos.append(6)
            else:
                changed_yaos.append(yao)
        
        changed_lower = tuple(changed_yaos[:3])
        changed_upper = tuple(changed_yaos[3:])
        changed_lower_name = gua_name_map.get(changed_lower, "乾")
        changed_upper_name = gua_name_map.get(changed_upper, "乾")
        changed_name = changed_upper_name + changed_lower_name
    else:
        changed_name = None
    
    gua_info = GUA_DATA.get(upper_name + lower_name, GUA_DATA.get("乾"))
    
    yaos_desc = []
    for i, yao in enumerate(yaos):
        yao_type = "老阴" if yao == 6 else "老阳" if yao == 9 else "少阴" if yao == 8 else "少阳"
        yaos_desc.append({
            "position": i + 1,
            "value": yao,
            "type": yao_type,
            "is_change": yao in [6, 9]
        })
    
    return {
        "hexagram": hexagram_name,
        "hexagram_info": gua_info,
        "yaos": yaos_desc,
        "change_yao": change_yao,
        "changed_hexagram": changed_name,
        "time": datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    }

