/**
 * 卦象占卜 - 根据问题生成卦象
 * @param {string} question - 问题
 * @returns {Object} 卦象结果
 */
function divinationGua(question) {
    // 使用当前时间作为随机种子
    const now = new Date();
    const seed = now.getTime() + (question ? question.length : 0);
    
    // 生成六个爻（6次随机）
    const yaos = [];
    for (let i = 0; i < 6; i++) {
        const random = (seed + i * 997) % 100;
        if (random < 15) yaos.push('6'); // 老阴（变爻）
        else if (random < 35) yaos.push('8'); // 少阴
        else if (random < 75) yaos.push('7'); // 少阳
        else yaos.push('9'); // 老阳（变爻）
    }
    
    // 生成卦象编码（从下到上）
    const guaCode = yaos.map(y => y === '6' || y === '8' ? '0' : '1').reverse().join('');
    const gua = GUA_DATA[guaCode] || GUA_DATA['111111']; // 默认乾卦
    
    // 确定变爻
    const changeYaos = [];
    yaos.forEach((y, idx) => {
        if (y === '6' || y === '9') {
            changeYaos.push({
                position: idx + 1,
                original: y,
                changed: y === '6' ? '9' : '6'
            });
        }
    });
    
    // 生成变卦
    const changeGuaCode = yaos.map(y => {
        if (y === '6') return '1';
        if (y === '9') return '0';
        return y === '8' ? '0' : '1';
    }).reverse().join('');
    const changeGua = GUA_DATA[changeGuaCode] || gua;
    
    return {
        question: question || '未指定问题',
        gua: gua,
        yaos: yaos,
        changeYaos: changeYaos,
        changeGua: changeGua,
        interpretation: generateGuaInterpretation(gua, changeYaos, question)
    };
}

