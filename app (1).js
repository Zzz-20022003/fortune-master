/**
 * 号码吉凶分析
 * @param {string} number - 号码（手机、车牌、门牌等）
 * @returns {Object} 分析结果
 */
function analyzeNumber(number) {
    // 提取数字
    const digits = number.replace(/\\D/g, '');
    
    if (digits.length === 0) {
        return {
            number: number,
            error: '请输入有效的数字'
        };
    }
    
    // 计算数字和
    let sum = 0;
    for (let d of digits) {
        sum += parseInt(d);
    }
    
    // 计算81数理
    let shuli = sum;
    while (shuli > 81) {
        shuli = Math.floor(shuli / 10) + (shuli % 10);
    }
    
    const shuliInfo = SHULI_JIXIONG[shuli] || { num: shuli, level: '中', title: '普通之数', desc: '吉凶参半，需要努力。', detail: '此数吉凶参半，需要根据实际情况来判断。' };
    
    // 分析数字组合
    const digitAnalysis = [];
    for (let i = 0; i < digits.length; i++) {
        const d = parseInt(digits[i]);
        digitAnalysis.push({
            position: i + 1,
            digit: d,
            meaning: getDigitMeaning(d)
        });
    }
    
    // 特殊组合分析
    const specialCombos = analyzeSpecialCombos(digits);
    
    return {
        number: number,
        digits: digits,
        sum: sum,
        shuli: shuliInfo,
        digitAnalysis: digitAnalysis,
        specialCombos: specialCombos,
        advice: generateNumberAdvice(shuliInfo, specialCombos)
    };
}

/**
 * 获取单个数字的含义
 */
function getDigitMeaning(digit) {
    const meanings = {
        0: '代表圆满、完整，也有虚无之意',
        1: '代表开始、独立、领导',
        2: '代表和谐、合作、平衡',
        3: '代表生长、发展、创造',
        4: '代表稳定、踏实，但也有死亡之意（谐音）',
        5: '代表变化、自由、冒险',
        6: '代表顺利、吉祥',
        7: '代表神秘、精神、幸运',
        8: '代表发财、富贵',
        9: '代表长久、圆满，但也有终结之意'
    };
    return meanings[digit] || '普通数字';
}

/**
 * 分析特殊数字组合
 */
function analyzeSpecialCombos(digits) {
    const combos = [];
    
    // 检查重复数字
    const repeats = digits.match(/(\\d)\\1{2,}/g);
    if (repeats) {
        repeats.forEach(r => {
            combos.push({
                type: '重复数字',
                combo: r,
                meaning: r[0] + '重复' + r.length + '次，' + (r[0] === '6' || r[0] === '8' || r[0] === '9' ? '非常吉利' : r[0] === '4' ? '需要注意' : '比较特殊')
            });
        });
    }
    
    // 检查顺子
    if (/012|123|234|345|456|567|678|789/.test(digits)) {
        combos.push({
            type: '顺子',
            combo: '顺子',
            meaning: '数字顺连，代表步步高升'
        });
    }
    
    // 检查对子
    if (/(.)(.)\\1\\2/.test(digits)) {
        combos.push({
            type: '对子',
            combo: '对子',
            meaning: '成双成对，代表和谐'
        });
    }
    
    // 检查豹子
    if (/(.)(.)(.)\\1\\2\\3/.test(digits)) {
        combos.push({
            type: '豹子',
            combo: '豹子',
            meaning: '三组重复，非常特殊'
        });
    }
    
    return combos.length > 0 ? combos : [{ type: '普通', combo: '-', meaning: '没有特殊组合' }];
}

