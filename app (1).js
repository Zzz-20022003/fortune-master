/**
 * 生成近期行动建议
 */
function generateActionAdvice(bazi, wuxing, mingju) {
    const months = ['一月', '二月', '三月', '四月', '五月', '六月', '七月', '八月', '九月', '十月', '十一月', '十二月'];
    const currentMonth = new Date().getMonth();
    
    const actionAdvice = [];
    
    for (let i = 0; i < 6; i++) {
        const monthIndex = (currentMonth + i) % 12;
        const monthZhi = DIZHI[(monthIndex + 2) % 12]; // 正月建寅
        const monthWuxing = WUXING[monthZhi];
        
        let advice = '';
        let avoid = '';
        
        if (monthWuxing === wuxing.strongest) {
            advice = '这个月你的运势较旺，适合积极进取，把握机会。';
            avoid = '不要过于骄傲自满，避免与人发生冲突。';
        } else if (monthWuxing === wuxing.weakest) {
            advice = '这个月运势较弱，适合稳健发展，不要冒险。';
            avoid = '避免做重大决定，不要进行高风险投资。';
        } else {
            advice = '这个月运势平稳，可以按部就班地工作生活。';
            avoid = '不要急于求成，避免过度劳累。';
        }
        
        actionAdvice.push({
            month: months[monthIndex],
            advice: advice,
            avoid: avoid
        });
    }
    
    return {
        title: '近期行动建议（未来6个月）',
        content: actionAdvice
    };
}


