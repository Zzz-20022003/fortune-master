/**
 * 获取年柱干支
 * @param {number} year - 公历年份
 * @returns {Object} {gan:天干, zhi:地支}
 */
function getGanZhiYear(year) {
    // 以1984年（甲子年）为基准
    const baseYear = 1984;
    const offset = year - baseYear;
    const ganIndex = ((offset % 10) + 10) % 10;
    const zhiIndex = ((offset % 12) + 12) % 12;
    return {
        gan: TIANGAN[ganIndex],
        zhi: DIZHI[zhiIndex],
        ganIndex: ganIndex,
        zhiIndex: zhiIndex
    };
}

/**
 * 获取月柱干支（考虑立春）
 * @param {number} year - 公历年份
 * @param {number} month - 公历月份（1-12）
 * @param {number} day - 公历日期
 * @returns {Object} {gan:天干, zhi:地支}
 */
function getGanZhiMonth(year, month, day) {
    // 立春通常在2月4日左右，简化处理以2月4日为界
    const liChunMonth = 2;
    const liChunDay = 4;
    
    // 确定年干（考虑立春）
    let yearGanIndex;
    if (month < liChunMonth || (month === liChunMonth && day < liChunDay)) {
        // 立春前，使用上一年
        const prevYear = year - 1;
        const baseYear = 1984;
        const offset = prevYear - baseYear;
        yearGanIndex = ((offset % 10) + 10) % 10;
    } else {
        const baseYear = 1984;
        const offset = year - baseYear;
        yearGanIndex = ((offset % 10) + 10) % 10;
    }
    
    // 月支：正月为寅
    let monthZhiIndex;
    if (month < liChunMonth || (month === liChunMonth && day < liChunDay)) {
        monthZhiIndex = (month + 10) % 12; // 立春前算上一年的月
    } else {
        monthZhiIndex = (month + 1) % 12;
    }
    
    // 月干根据年干推算（五虎遁月）
    // 甲己之年丙作首，乙庚之岁戊为头，丙辛之岁寻庚起，丁壬壬位顺行流，戊癸何方发，甲寅之上好追求
    const monthGanStart = [2, 4, 6, 8, 0]; // 丙戊庚壬甲的索引
    const yearGanMod5 = yearGanIndex % 5;
    const monthGanIndex = (monthGanStart[yearGanMod5] + monthZhiIndex) % 10;
    
    return {
        gan: TIANGAN[monthGanIndex],
        zhi: DIZHI[monthZhiIndex],
        ganIndex: monthGanIndex,
        zhiIndex: monthZhiIndex
    };
}

/**
 * 获取日柱干支
 * @param {number} year - 公历年份
 * @param {number} month - 公历月份（1-12）
 * @param {number} day - 公历日期
 * @returns {Object} {gan:天干, zhi:地支}
 */
function getGanZhiDay(year, month, day) {
    // 以1900年1月31日为基准（甲子日）
    const baseDate = new Date(1900, 0, 31);
    const targetDate = new Date(year, month - 1, day);
    const diffDays = Math.floor((targetDate - baseDate) / (1000 * 60 * 60 * 24));
    
    const ganIndex = ((diffDays % 10) + 10) % 10;
    const zhiIndex = ((diffDays % 12) + 12) % 12;
    
    return {
        gan: TIANGAN[ganIndex],
        zhi: DIZHI[zhiIndex],
        ganIndex: ganIndex,
        zhiIndex: zhiIndex
    };
}

/**
 * 获取时柱干支
 * @param {string} dayGan - 日干
 * @param {number} hour - 小时（0-23）
 * @returns {Object} {gan:天干, zhi:地支}
 */
function getGanZhiHour(dayGan, hour) {
    // 确定时支
    let zhiIndex;
    if (hour >= 23 || hour < 1) zhiIndex = 0; // 子
    else if (hour < 3) zhiIndex = 1; // 丑
    else if (hour < 5) zhiIndex = 2; // 寅
    else if (hour < 7) zhiIndex = 3; // 卯
    else if (hour < 9) zhiIndex = 4; // 辰
    else if (hour < 11) zhiIndex = 5; // 巳
    else if (hour < 13) zhiIndex = 6; // 午
    else if (hour < 15) zhiIndex = 7; // 未
    else if (hour < 17) zhiIndex = 8; // 申
    else if (hour < 19) zhiIndex = 9; // 酉
    else if (hour < 21) zhiIndex = 10; // 戌
    else zhiIndex = 11; // 亥
    
    // 时干根据日干推算（五鼠遁时）
    // 甲己还加甲，乙庚丙作初，丙辛从戊起，丁壬庚子居，戊癸何方发，壬子是真途
    const dayGanIndex = TIANGAN.indexOf(dayGan);
    const hourGanStart = [0, 2, 4, 6, 8]; // 甲丙戊庚壬的索引
    const dayGanMod5 = dayGanIndex % 5;
    const ganIndex = (hourGanStart[dayGanMod5] + zhiIndex) % 10;
    
    return {
        gan: TIANGAN[ganIndex],
        zhi: DIZHI[zhiIndex],
        ganIndex: ganIndex,
        zhiIndex: zhiIndex
    };
}

/**
 * 计算八字
 * @param {number} year - 公历年份
 * @param {number} month - 公历月份
 * @param {number} day - 公历日期
 * @param {number} hour - 小时（0-23）
 * @returns {Object} 八字信息
 */
function calculateBazi(year, month, day, hour) {
    const yearPillar = getGanZhiYear(year);
    const monthPillar = getGanZhiMonth(year, month, day);
    const dayPillar = getGanZhiDay(year, month, day);
    const hourPillar = getGanZhiHour(dayPillar.gan, hour);
    
    // 获取生肖
    const shengxiao = SHENGXIAO[yearPillar.zhiIndex];
    
    // 获取时辰名称
    let shichenName;
    for (let sc of SHICHEN) {
        if (hour >= sc.start && hour < sc.end) {
            shichenName = sc.name;
            break;
        }
    }
    if (!shichenName) shichenName = '子时';
    
    return {
        year: yearPillar,
        month: monthPillar,
        day: dayPillar,
        hour: hourPillar,
        shengxiao: shengxiao,
        shichen: shichenName,
        dayGan: dayPillar.gan,
        dayZhi: dayPillar.zhi
    };
}

/**
 * 获取十神
 * @param {string} dayGan - 日干
 * @param {string} targetGan - 目标天干
 * @returns {string} 十神名称
 */
function getShiShen(dayGan, targetGan) {
    const dayGanIndex = TIANGAN.indexOf(dayGan);
    const targetGanIndex = TIANGAN.indexOf(targetGan);
    
    const dayGanWuxing = WUXING[dayGan];
    const targetGanWuxing = WUXING[targetGan];
    
    const dayGanYinYang = dayGanIndex % 2 === 0 ? '阳' : '阴';
    const targetGanYinYang = targetGanIndex % 2 === 0 ? '阳' : '阴';
    
    // 五行相生关系
    const shengRelation = {
        '木': '火', '火': '土', '土': '金', '金': '水', '水': '木'
    };
    
    // 五行相克关系
    const keRelation = {
        '木': '土', '土': '水', '水': '火', '火': '金', '金': '木'
    };
    
    // 判断关系
    if (targetGan === dayGan) {
        return '比肩';
    }
    
    if (targetGanWuxing === dayGanWuxing) {
        return targetGanYinYang !== dayGanYinYang ? '劫财' : '比肩';
    }
    
    if (shengRelation[dayGanWuxing] === targetGanWuxing) {
        return targetGanYinYang === dayGanYinYang ? '食神' : '伤官';
    }
    
    if (shengRelation[targetGanWuxing] === dayGanWuxing) {
        return targetGanYinYang === dayGanYinYang ? '偏印' : '正印';
    }
    
    if (keRelation[dayGanWuxing] === targetGanWuxing) {
        return targetGanYinYang === dayGanYinYang ? '偏财' : '正财';
    }
    
    if (keRelation[targetGanWuxing] === dayGanWuxing) {
        return targetGanYinYang === dayGanYinYang ? '七杀' : '正官';
    }
    
    return '未知';
}

/**
 * 计算五行
 * @param {Object} bazi - 八字对象
 * @returns {Object} 五行统计
 */
function calculateWuxing(bazi) {
    const counts = { '金': 0, '木': 0, '水': 0, '火': 0, '土': 0 };
    const details = [];
    
    // 统计四柱天干的五行
    const pillars = ['year', 'month', 'day', 'hour'];
    const pillarNames = { 'year': '年柱', 'month': '月柱', 'day': '日柱', 'hour': '时柱' };
    
    for (let pillar of pillars) {
        const gan = bazi[pillar].gan;
        const zhi = bazi[pillar].zhi;
        const ganWuxing = WUXING[gan];
        const zhiWuxing = WUXING[zhi];
        
        counts[ganWuxing]++;
        counts[zhiWuxing]++;
        
        details.push({
            pillar: pillarNames[pillar],
            gan: gan,
            ganWuxing: ganWuxing,
            zhi: zhi,
            zhiWuxing: zhiWuxing
        });
        
        // 统计地支藏干
        const canggan = DIZHI_CANGGAN[zhi];
        for (let cg of canggan) {
            const cgWuxing = WUXING[cg];
            counts[cgWuxing] += 0.5; // 藏干权重减半
        }
    }
    
    // 找出最旺和最弱的五行
    let maxWuxing = '';
    let minWuxing = '';
    let maxCount = -1;
    let minCount = 999;
    
    for (let wx in counts) {
        if (counts[wx] > maxCount) {
            maxCount = counts[wx];
            maxWuxing = wx;
        }
        if (counts[wx] < minCount) {
            minCount = counts[wx];
            minWuxing = wx;
        }
    }
    
    return {
        counts: counts,
        details: details,
        strongest: maxWuxing,
        weakest: minWuxing,
        dayGanWuxing: WUXING[bazi.day.gan]
    };
}

