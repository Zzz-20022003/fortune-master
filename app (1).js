/**
 * 周公解梦
 * @param {string} dream - 梦境描述
 * @returns {Object} 解梦结果
 */
function interpretDream(dream) {
    if (!dream || dream.trim().length === 0) {
        return {
            dream: dream,
            error: '请输入梦境描述'
        };
    }
    
    // 关键词匹配
    const keywords = Object.keys(DREAM_DATA);
    const matchedKeywords = [];
    
    for (let keyword of keywords) {
        if (dream.includes(keyword)) {
            matchedKeywords.push(keyword);
        }
    }
    
    // 如果没有匹配到关键词，尝试模糊匹配
    if (matchedKeywords.length === 0) {
        // 简单模糊匹配
        const fuzzyMatches = {
            '水': ['河', '湖', '海', '雨', '雪', '冰'],
            '火': ['烧', '热', '灯', '烛', '焰'],
            '蛇': ['蟒', '蛟', '龙'],
            '飞': ['飘', '升', '空'],
            '掉牙': ['牙', '齿'],
            '考试': ['试', '卷', '题', '答'],
            '追赶': ['追', '逃', '跑'],
            '死亡': ['死', '亡', '尸', '鬼'],
            '结婚': ['婚', '嫁', '娶', '礼'],
            '怀孕': ['孕', '胎', '生'],
            '房子': ['房', '屋', '家', '楼'],
            '钱': ['钱', '财', '金', '银'],
            '车': ['车', '驾', '驶'],
            '山': ['山', '峰', '岭'],
            '桥': ['桥', '梁'],
            '树': ['树', '木', '林'],
            '衣服': ['衣', '服', '穿'],
            '食物': ['食', '吃', '饭', '菜'],
            '动物': ['兽', '禽', '虫']
        };
        
        for (let key in fuzzyMatches) {
            for (let synonym of fuzzyMatches[key]) {
                if (dream.includes(synonym)) {
                    matchedKeywords.push(key);
                    break;
                }
            }
        }
    }
    
    // 生成解梦结果
    const interpretations = [];
    for (let keyword of matchedKeywords) {
        if (DREAM_DATA[keyword]) {
            interpretations.push(DREAM_DATA[keyword]);
        }
    }
    
    // 如果没有匹配到，给出通用解释
    if (interpretations.length === 0) {
        interpretations.push({
            title: '通用解梦',
            meaning: '梦境是潜意识的反映',
            detail: '你的梦境' + dream + '可能反映了你最近的心理状态。建议你回顾最近的生活，看看是否有相关的事情在困扰你。保持心情愉快，不要过度解读梦境。'
        });
    }
    
    return {
        dream: dream,
        keywords: matchedKeywords,
        interpretations: interpretations,
        summary: generateDreamSummary(interpretations)
    };
}

/**
 * 生成解梦总结
 */
function generateDreamSummary(interpretations) {
    if (interpretations.length === 1) {
        return interpretations[0].detail;
    }
    
    let summary = '你的梦境包含多个元素：\\n';
    interpretations.forEach((interp, idx) => {
        summary += (idx + 1) + '.' + interp.title + '：' + interp.meaning + '\\n';
    });
    summary += '\\n综合来看，这个梦境' + (interpretations.length > 2 ? '比较复杂，可能反映了你多方面的担忧或期待。' : '反映了你当前的心理状态。') + '建议你保持平和的心态，不要过于担心。';
    
    return summary;
}


